myTuple = ("Sam", "Terry", "Alvin", "Carol")
print(myTuple)              #('Sam', 'Terry', 'Alvin', 'Carol')
print(type(myTuple))        #<class 'tuple'> 
t = "#".join(myTuple)
print(t)                    #Sam#Terry#Alvin#Carol
print(type(t))              #<class 'str'>

myList = ["Sam", "Terry", "Alvin", "Carol"]
print(myList)               #('Sam', 'Terry', 'Alvin', 'Carol')
print(type(myList))         #<class 'list'>
l = ".".join(myList)
print(l)                    #Sam.Terry.Alvin.Carol
print(type(l))              #<class 'str'>  

